    public static int convertFrom(String operand) throws NoRomanNumberException {
        for (int i = 1; i < romanNumbers.length; i++) {
            if(operand.equalsIgnoreCase(romanNumbers[i])) return i;
        }
        throw new NoRomanNumberException();
    }

    public static String convertTo(int value) throws NoRomanNumberException {
        if(value > 0 && value < romanNumbers.length) return romanNumbers[value];
        throw new NoRomanNumberException();
    }


